package atividades;

public class OperadoresLogicos {
    public static void main(String[] args) {
        int batata = 50;
        int abacate = 3;
        int resultado = batata + abacate;
        System.out.println("Batata = " + batata + " Abacate = " + abacate + " Total " + resultado);

        byte banana = 100;
        byte morango = 25;
        resultado = banana - morango;
        System.out.println("Banana = " + banana + " Morango = " + morango + " Total " + resultado);

        short tomate = 49;
        short pepino = 19;
        resultado = tomate * pepino;
        System.out.println("Tomate = " + tomate + " Pepino = " + pepino + " Total " + resultado);

        byte janela = 10;
        byte porta = 2;
        resultado = janela / porta;
        System.out.println("Janela = " + janela + " Porta = " + porta + " Total " + resultado);
    }
}
